package model.clasification;

public class ClasificationDto {
	public String dni;
	public String idcarrera;
	public String posicion;
	public String sexo;
	public String tiempoInicio;
	public String tiempoFinal;	
	public String categoryname;
	public String dorsal;
	public String club;
	public String getDni() {
		return dni;
	}
	public void setDni(String dni) {
		this.dni = dni;
	}
	public String getIdcarrera() {
		return idcarrera;
	}
	public void setIdcarrera(String idcarrera) {
		this.idcarrera = idcarrera;
	}
	public String getPosicion() {
		return posicion;
	}
	public void setPosicion(String posicion) {
		this.posicion = posicion;
	}
	public String getSexo() {
		return sexo;
	}
	public void setSexo(String sexo) {
		this.sexo = sexo;
	}
	public String getTiempoInicio() {
		return tiempoInicio;
	}
	public void setTiempoInicio(String tiempoInicio) {
		this.tiempoInicio = tiempoInicio;
	}
	public String getTiempoFinal() {
		return tiempoFinal;
	}
	public void setTiempoFinal(String tiempoFinal) {
		this.tiempoFinal = tiempoFinal;
	}
	public String getCategoryname() {
		return categoryname;
	}
	public void setCategoryname(String categoryname) {
		this.categoryname = categoryname;
	}
	public String getDorsal() {
		return dorsal;
	}
	public void setDorsal(String dorsal) {
		this.dorsal = dorsal;
	}
	public String getClub() {
		return club;
	}
	public void setClub(String club) {
		this.club = club;
	}
	
	
}
